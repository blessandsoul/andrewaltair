const CUSTOM_TEMPLATES_KEY = 'andrewaltair_custom_templates'

export function getCustomTemplates(): PostTemplate[] {
    if (typeof window === 'undefined') return []
    try {
        const stored = localStorage.getItem(CUSTOM_TEMPLATES_KEY)
        return stored ? JSON.parse(stored) : []
    } catch {
        return []
    }
}

export function saveCustomTemplate(template: Omit<PostTemplate, 'id'>): PostTemplate {
    const newTemplate: PostTemplate = {
        ...template,
        id: `custom-${Date.now()}`
    }
    const existing = getCustomTemplates()
    localStorage.setItem(CUSTOM_TEMPLATES_KEY, JSON.stringify([...existing, newTemplate]))
    return newTemplate
}

export function deleteCustomTemplate(id: string): void {
    const existing = getCustomTemplates()
    localStorage.setItem(CUSTOM_TEMPLATES_KEY, JSON.stringify(existing.filter(t => t.id !== id)))
}

// Get all templates (preset + custom)
export function getAllTemplates(): PostTemplate[] {
    return [...POST_TEMPLATES, ...getCustomTemplates()]
}
